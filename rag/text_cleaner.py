import re
def clean_text(text):
    text = re.sub(r'^\s*\d+\s*$', '', text, flags=re.MULTILINE)
    text = re.sub(r'^\s*[Pp]age\s+\d+\s*$', '', text, flags=re.MULTILINE)
    text = re.sub(r'https?:\/\/\S+|www\.\S+', '', text, flags=re.MULTILINE)
    text = re.sub(r'^\s*(©|Copyright|All rights reserved).*$', '', text, flags=re.MULTILINE)
    text = re.sub(r'^[\-\_\*\=\~]{3,}$', '', text, flags=re.MULTILINE)
    text = re.sub(r'^.{2,}[.…]{4,}\s*\d*\s*$', '', text, flags=re.MULTILINE)
    text = re.sub(r'^.{0,60}(Annual Report|Integrated Report|Annual Review)\s*[\|:]?\s*\d{4}[-–]\d{2,4}.*$', '', text, flags=re.MULTILINE)
    text = re.sub(r'^\s*[A-Z]{1,2}(\s+[A-Z]{1,2}){1,6}\s*$', '', text, flags=re.MULTILINE)
    text = re.sub(r'^\s*\d+\.?\d*\s*%\s*$', '', text, flags=re.MULTILINE)
    text = re.sub(r'^\s*[₹$€£¥]\s*[\d,\.]+\s*$', '', text, flags=re.MULTILINE)
    text = re.sub(r'^\s*[^a-zA-Z0-9\n]{3,}\s*$', '', text, flags=re.MULTILINE)
    text = re.sub(r'[A-Z]\s[A-Z](?:Board|Committee|Chairman|Member)', '', text)
    text = re.sub(r'(?<!\w)\d{1,3}(?=[A-Z])', '', text)
    text = re.sub(r'^\s*(Jan|Feb|Mar|Apr|May|Jun|Jul|Aug|Sep|Oct|Nov|Dec)\s*$', '', text, flags=re.MULTILINE)
    text = re.sub(r'^\s*Q[1-4]\s*$', '', text, flags=re.MULTILINE)
    text = re.sub(r'^\s*[\d,\.\s%₹$€£]+\s*$', '', text, flags=re.MULTILINE)
    text = re.sub(r'\bT\s+eam\b', 'Team', text)
    text = re.sub(r'\bT\s+enure\b', 'Tenure', text)
    text = text.replace("\t", " ")
    text = text.replace("\xa0", " ")
    text = text.replace("\u200b", "")
    text = text.replace("\uf0b7", "")
    text = re.sub(r" {2,}", " ", text)
    text = re.sub(r"\n{3,}", "\n\n", text)
    text = re.sub(r'/[a-z]+_t\.liga', '', text)
    text = text.replace('/uni20B9', '₹')
    return text